/// Represents different types of materials that a wall can be made of.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Material {
    Empty,
    SolidWall,
    BrickWall,
    StoneWall,
    WoodWall,
    OutOfBounds,
}

impl From<u8> for Material {
    fn from(val: u8) -> Self {
        match val {
            0 => Material::Empty,
            1 => Material::SolidWall,
            2 => Material::BrickWall,
            3 => Material::StoneWall,
            4 => Material::WoodWall,
            _ => Material::OutOfBounds,
        }
    }
}

